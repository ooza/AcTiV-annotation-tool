    private void jbFileChooserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFileChooserActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setMultiSelectionEnabled(false);
        chooser.setFileFilter(new FileNameExtensionFilter("M4 Videos", "mp4"));
        int returnVal = chooser.showOpenDialog(null);
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            jtfFileUrl.setText(chooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jbFileChooserActionPerformed

    private void jbApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbApplyActionPerformed
        if (jtfChannel.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please enter channel name.");
        else if (jtfResolution.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please enter video resolution.");
        else if (jtfOrientation.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please enter scrolling text orientation.");
        else if (jtfResolution.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please enter video resolution.");
        else if (jtfBGUrl.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please enter scrolling text background image URL.");
        else{
            if (!jtfFileUrl.getText().equals("")){
                MainFrame frm = new MainFrame();
                frm.doLoadVideoFromFile(new File(jtfFileUrl.getText()));
                frm.setVisible(true);
                this.dispose();
            }else if (!jtfFolderUrl.getText().equals(""))
            {
                MainFrame frm = new MainFrame();
                frm.doLoadImagesFromFile(new File(jtfFolderUrl.getText()));
                frm.setVisible(true);
                this.dispose();
            }
            else
                JOptionPane.showMessageDialog(null, "Please select a Video file or frames directory.");
        }
            
    }//GEN-LAST:event_jbApplyActionPerformed

