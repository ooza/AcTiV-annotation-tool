    private void jbFileChooser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFileChooser1ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setAcceptAllFileFilterUsed(false);
        chooser.setMultiSelectionEnabled(true);
        int returnVal = chooser.showOpenDialog(null);
        if(returnVal == JFileChooser.APPROVE_OPTION) {
            jtfFolderUrl.setText(chooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jbFileChooser1ActionPerformed

